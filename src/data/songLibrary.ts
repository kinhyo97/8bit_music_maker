const notesToEvents = (bar: number, notes: string[], length = "16n", beatGrid = sixteenthBeats) =>
  notes.map((note, index) => ({
    bar,
    beat: beatGrid[index],
    note,
    length,
  }));

const chordHits = (bar: number, beats: number[], notes: string[], length = "16n") =>
  beats.flatMap((beat) =>
    notes.map((note) => ({
      bar,
      beat,
      note,
      length,
    })),
  );

const repeatedDrumBeats = (bars: number[], beats: number[]) =>
  bars.flatMap((bar) => beats.map((beat) => ({ bar, beat })));

